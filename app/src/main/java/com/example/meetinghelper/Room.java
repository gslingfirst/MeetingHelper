package com.example.meetinghelper;

public class Room {
    private int room_id;
    private String name;
    private String location;
    private String desc;

    public int getRoom_id() {
        return room_id;
    }

    public String getName() {
        return name;
    }

    public String getLocation() {
        return location;
    }

    public String getDesc() {
        return desc;
    }
}
